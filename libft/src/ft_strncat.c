#include <unistd.h>

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t		j;
	size_t		i;

	i = 0;
	j = 0;
	while (s1[i])
		i++;
	while (s2[j] && j < n)
	{
		s1[i] = s2[j];
		j++;
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
